package encapsulation;

public class ConstructorMethod {
	String brand;
	int price;
	String colour;
	boolean isHaveFan;
	
	
	public ConstructorMethod(String brand, int price, String colour, boolean isHaveFan) {
		super();
		this.brand = brand;
		this.price = price;
		this.colour = colour;
		this.isHaveFan = isHaveFan;
	}
}

